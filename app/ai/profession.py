from app.ai.client import client
model = "gemini-1.5-flash"

async def response_by_profession_subjects(profession_name: str):
    try:
        prompt = f'''Выдать список школьных предметов, нужных для сдачи ЕГЭ в 2025/2026 г. для поступления на профессию "{profession_name}" в г. Москва. Оформить в деловом стиле. Напиши без лишних слов. Например: В соответствии с требованиями для поступления на медицинские специальности вузов Москвы в 2025/2026 учебном году, представлен список обязательных и рекомендованных предметов для сдачи ЕГЭ:
    
    1. **Русский язык**  
       - Обязательный предмет для всех абитуриентов.
    
    2. **Биология**  
       - Обязательный предмет для поступления на медицинские направления.
    
    3. **Химия**  
       - Основной предмет для изучения медицинских дисциплин.
    
    Дополнительно может потребоваться сдача следующих предметов (в зависимости от требований конкретного вуза):
    
    4. **Математика**  
       - Рекомендуется профильный уровень для закрепления научного подхода.
    
    5. **Физика**  
       - Полезный предмет для общего понимания естественных наук.
    
    При подготовке к ЕГЭ абитуриентам рекомендуется ознакомиться с требованиями выбранных вузов, так как они могут варьироваться.'''

        response = client.generate_content(
            model=model,
            prompt=prompt,
            temperature=0.7,
            max_output_tokens=400,
        )
        return response.candidates[0].output_text

    except Exception as e:
        print("Gemini error:", e)
        return "Извините, сервис временно недоступен. Попробуйте позже."




